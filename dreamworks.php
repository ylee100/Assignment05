        <h2>About</h2>
         <p>DreamWorks Animation, L.L.C. (more commonly known as DreamWorks Animation and DreamWorks Animation SKG, or simply DreamWorks) is an American animation studio that is a subsidiary of Universal Pictures, a division of Comcast through its wholly owned subsidiary NBCUniversal. It is based in Glendale, California and produces animated feature films, television programs and online virtual games. The studio has currently released a total of 35 feature films, including the franchises Shrek, Madagascar, Kung Fu Panda, How to Train Your Dragon, The Croods, Trolls and The Boss Baby. Originally formed under the banner of its main DreamWorks studio in 1997 by some of Amblin Entertainment's former animation branch Amblimation alumni, it was spun off into a separate public company in 2004. DreamWorks Animation currently maintains its Glendale campus, as well as satellite studios in India and China. On August 22, 2016, NBCUniversal acquired DreamWorks Animation for $3.8 billion, making it a division of the Universal Filmed Entertainment Group as an acquisition for the animation studio.</p>
