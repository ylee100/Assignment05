        <h2>Brief History</h2>
        <h3>Foundation</h3>
        <p>On October 12, 1994, a trio of entertainment players, film director and producer Steven Spielberg, former Disney executive Jeffrey Katzenberg, and music executive David Geffen, founded DreamWorks SKG. To build the talent base, Spielberg brought over artists from his London-based studio, Amblimation, while Katzenberg recruited some of the top animation staff from Disney. Some of Amblimation's artists came to DreamWorks in 1995, when the studio's last feature was completed, with the rest doing so following the studio's closure in 1997.</p>
        <h3>Today</h3>
        <p>On February 2, 2018, CMC Capital Partners bought DreamWorks', Shanghai Media Group's, and Shanghai Alliance Investment's stake in Oriental DreamWorks, owning the studio in its entirety; Oriental DreamWorks was then renamed "Pearl Studio". Pearl Studio will still collaborate with DreamWorks to produce the upcoming film, Everest, with the film's original director, Jill Culton, returning.</p>
